
public class GameArea {

	private int x;
	private int y;
	private int height;
	private int width;

	public GameArea(int x, int y, int h, int w) {

		this.x = x;
		this.y = y;
		this.width = w;
		this.height = h;

	}

	public int getX() {

		return this.x;

	}

	public int getY() {

		return this.y;

	}

	public int getHeight() {

		return this.height;

	}

	public int getWidth() {

		return this.width;

	}
	

}